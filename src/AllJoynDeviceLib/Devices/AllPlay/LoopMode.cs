namespace AllJoynClientLib.Devices.AllPlay
{
    /// <summary>
    /// Playlist loop mode
    /// </summary>
    public enum LoopMode
    {
        /// <summary>
        /// Don't loop
        /// </summary>
        None,

        /// <summary>
        /// Repeat the current track
        /// </summary>
        One,

        /// <summary>
        /// Repeat the entire playlist
        /// </summary>
        All
    }
}
